function HexToRGB(hex)
  local rhex, ghex, bhex = string.sub(hex, 2, 3), string.sub(hex, 4, 5), string.sub(hex, 6, 7)
  return tonumber(rhex, 16), tonumber(ghex, 16), tonumber(bhex, 16)
end

function easyRotation:LoadSpellInfo()
  if easyRotation.rotation ~= nil then
    for spell, info in pairs(easyRotation.spells[easyRotation.rotation]) do
      -- update rgb color
      if easyRotation.spells[easyRotation.rotation][spell]["color"] and type(easyRotation.spells[easyRotation.rotation][spell]["color"]) == "string" then
        local r, g, b = HexToRGB(easyRotation.spells[easyRotation.rotation][spell]["color"])
        easyRotation.spells[easyRotation.rotation][spell]["color"] = {["r"] = r, ["g"] = g, ["b"] = b}
      end
      local spellname = spell
      if string.sub(spell,-string.len("(Not Ready)")) == "(Not Ready)" then
        spellname = string.sub(spell,0,-(string.len("(Not Ready)")+1)):gsub("^%s*(.-)%s*$", "%1")
        easyRotation.spells[easyRotation.rotation][spell]["color"] = {["r"] = 127, ["g"] = 127, ["b"] = 127}
      end
      local name, rank, icon, castTime, minRange, maxRange = GetSpellInfo(spellname);
      if(castingTime ~= nil) then
        easyRotation.spells[easyRotation.rotation][spell].castTime = castingTime / 1000.0; -- seconds
      end
      if(icon ~= nil) then
        easyRotation.spells[easyRotation.rotation][spell].icon = icon
      end
    end
  end
end

